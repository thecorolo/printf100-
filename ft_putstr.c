#include "ft_printf.h"

void	ft_putstr(const char *str, int *len)
{
	int	i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			write(1, &str[i], 1);
			i++;
			(*len)++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		*len += 6;
	}
}
